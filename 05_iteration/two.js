// while loop
let index = 0;
while (index <= 10) {
  //   console.log(`The value of the index is : ${index}`);
  index = index + 2;
}

let myArr = ["batman", "superman", "flash"];
let arr = 0;
while (arr < myArr.length) {
  //   console.log(`Values of arr : ${myArr[arr]}`);
  arr++;
}

